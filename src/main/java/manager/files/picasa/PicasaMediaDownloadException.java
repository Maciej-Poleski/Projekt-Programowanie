package manager.files.picasa;

public class PicasaMediaDownloadException extends Exception {

	private static final long serialVersionUID = -3408348332599032149L;

	public PicasaMediaDownloadException() {
		super();
	}

	public PicasaMediaDownloadException(String msg) {
		super(msg);
	}

	public PicasaMediaDownloadException(Throwable t) {
		super(t);
	}

	public PicasaMediaDownloadException(String msg, Throwable t) {
		super(msg, t);
	}

}
