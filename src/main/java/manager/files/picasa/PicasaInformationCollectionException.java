package manager.files.picasa;

public class PicasaInformationCollectionException extends Exception {

	private static final long serialVersionUID = -3408348332599032149L;

	public PicasaInformationCollectionException() {
		super();
	}

	public PicasaInformationCollectionException(String msg) {
		super(msg);
	}

	public PicasaInformationCollectionException(Throwable t) {
		super(t);
	}

	public PicasaInformationCollectionException(String msg, Throwable t) {
		super(msg, t);
	}

}
